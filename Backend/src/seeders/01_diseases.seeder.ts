// ── Seeder ───────────────────────────────────────────────────────────────────

const CONTEXT = '01_diseases.seeder';

export async function seedDiseases(): Promise<void> {
  logger.info(`Iniciando seeder de enfermedades (${DISEASES.length} registros)`, CONTEXT);

  let created = 0;
  let skipped = 0;

  for (const d of DISEASES) {
    const slug = slugify(d.name);
    const normalizedName = normalize(d.name);

    const [, wasCreated] = await Disease.findOrCreate({
      where: { slug },
      defaults: {
        name: d.name,
        normalizedName,
        slug,
        description: d.description,
        category: d.category,
        severity: d.severity,
        isContagious: d.isContagious,
        isZoonotic: d.isZoonotic,
        defaultQuarantineDays: d.defaultQuarantineDays ?? undefined,
        incubationDaysMin: d.incubationDaysMin ?? undefined,
        incubationDaysMax: d.incubationDaysMax ?? undefined,
        recommendedAction: d.recommendedAction,
        affectedSystems: d.affectedSystems,
        isActive: true,
      },
    });

    if (wasCreated) created++;
    else skipped++;
  }

  logger.info(`Enfermedades — creadas: ${created}, ya existían: ${skipped}`, CONTEXT);
}
